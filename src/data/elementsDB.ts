// 元素を原子番号で取得
export function getElementByAtomicNumber(atomicNumber: number): ElementData | undefined {
  return elementsDB[atomicNumber];
}

// 元素を記号で取得
export function getElementBySymbol(symbol: string): ElementData | undefined {
  return Object.values(elementsDB).find(el => el.symbol === symbol);
}

// すべての元素を取得
export function getAllElements(): ElementData[] {
  return Object.values(elementsDB).sort((a, b) => a.atomicNumber - b.atomicNumber);
}

// 電子殻の設定
export interface ElectronShell {
  electrons: number;
  radius: number;
}

// 元素のカテゴリ
export type ElementCategory =
  | 'alkali-metal'
  | 'alkaline-earth'
  | 'transition-metal'
  | 'post-transition'
  | 'metalloid'
  | 'nonmetal'
  | 'halogen'
  | 'noble-gas'
  | 'lanthanide'
  | 'actinide';

// 元素データの型定義
export interface ElementData {
  atomicNumber: number;
  symbol: string;
  name: string;
  nameJa: string;
  protons: number;
  neutrons: number; // 最も安定な同位体の中性子数
  electronShells: ElectronShell[];
  nucleusRadius?: number;
  particleRadius?: number;
  // 周期表の位置 (1-indexed)
  period?: number; // 周期 (行)
  group?: number; // 族 (列)
  category?: ElementCategory;
}

